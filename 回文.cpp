#include<stdio.h>
main()
{
	int n,b[100],a,i,k;
	bool is=true;
	scanf("%d",&n);
	while(n)
	{
	  scanf("%d",&a);
	  for(i=0;a>0;i++)
	  {
	   b[i]=a%10;
	   a/=10;
	  }
	  k=i;
	  for(i=0;i<k;i++)
	  {
	   if(b[i]!=b[k-i-1])
	    {
		 is=false;
		 break;
	    }
	  }
	  if(is)
	   printf("yes\n");
	  else
	   printf("no\n");
	  n--;
	}
}
